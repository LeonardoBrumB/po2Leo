    private void logar() {

        if (new String(txtSenha.getPassword()).equals("")) {
            JOptionPane.showMessageDialog(null, "O Campo 'senha' est√° em branco");
            return;
        }

        
        if (new String(txtSenha.getPassword()).equals("321")) {
            this.dispose();
            new FRMenuFuncionario(null, rootPaneCheckingEnabled).setVisible(true);
            
        }else{
            JOptionPane.showMessageDialog(null,"Senha incorreta!");
            this.dispose();
            new FRMenu().setVisible(true);
        }
    }
